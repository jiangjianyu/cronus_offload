    // arr[0] = 0x4b70e6bd552008d4;
    // arr[1] = 0xf2e1663c12ba348d;

    auto r = cuGetExportTable(&table, &id);
    if (r == CUDA_SUCCESS) {
        fprintf(stderr, "table %lx\n", (unsigned long int)table);
        long* out = (long*)table;
        for (int i = 0;i <= out[0]/sizeof(void*);i++) {
            fprintf(stderr, "func %d -> %lx (%lx)\n", i, out[i], out[i] - base_addr);
            if (out[i] == 0)
                break;
        }
    } else {
        fprintf(stderr, "table is not found %d\n", r);
    }
    return 0;
