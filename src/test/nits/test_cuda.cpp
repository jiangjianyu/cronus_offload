    CUuuid id;

    long* arr = (long*)id.bytes;

    long base_addr = 0;

    auto handle = dlopen(NULL, RTLD_LAZY);
    for (int i = 0;i < sizeof(driver_table) / sizeof(void*);i++) {
        auto api_addr = dlsym(handle, driver_table[i]);
        auto jmp_addr = (char*)api_addr + 0xc;
        auto jmp_addr_char = (unsigned char*) jmp_addr;
        if (jmp_addr_char[0] != 0xff) {
            fprintf(stderr, "%lx (%lx) %s no indirect addr\n", (unsigned long int)api_addr, (unsigned long int)api_addr - base_addr, driver_table[i]);
            continue;
        }
        int relative = jmp_addr_char[2] + ((int)jmp_addr_char[3]) * 256 + ((int)jmp_addr_char[4]) * 256 * 256 + ((int)jmp_addr_char[5]) * 256 * 256 * 256;
        long *target_addr = (long*)(jmp_addr + relative + 0x6);
        fprintf(stderr, "%lx (%lx) %lx (%lx) -> %s\n", (unsigned long int)api_addr, (unsigned long int)api_addr - base_addr, *target_addr, *target_addr - base_addr, driver_table[i]);
    }

    // 7 functions
    arr[0] = 0x4ae7f45b6cfbd56b;
    arr[1] = 0xf99dfd1239d98789;

    // 10 functions
